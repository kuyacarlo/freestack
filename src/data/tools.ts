export function filterTools(
  list: Tool[],
  opts: {
    q?: string;
    cost?: Cost | "all";
    student?: Student | "all";
    commercial?: Commercial | "all";
    category?: Category | "all";
  },
): Tool[] {
  const q = opts.q?.trim().toLowerCase() ?? "";
  return list.filter((t) => {
    if (opts.cost && opts.cost !== "all" && t.cost !== opts.cost) return false;
    if (opts.student && opts.student !== "all" && t.student !== opts.student)
      return false;
    if (
      opts.commercial &&
      opts.commercial !== "all" &&
      t.commercial !== opts.commercial
    )
      return false;
    if (
      opts.category &&
      opts.category !== "all" &&
      t.category !== opts.category
    )
      return false;
    if (!q) return true;
    const hay = `${t.name} ${t.blurb} ${t.limits} ${t.tags?.join(" ") ?? ""}`.toLowerCase();
    return hay.includes(q);
  });
}
